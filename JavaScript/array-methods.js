// Array litral [] to define the array
var users = ["Rohit", "Anil", "Pawan"];

//new Array() method for define the array

var users = new Array("Rohit", "Anil", "Pawan");

// length property
users.length // output will be 3

// Array.isArray() method to know the typeof array
Array.isArray(users); // output will be true

//toString() method Converting Arrays to Strings

users.toString()// Output will be 'Rohit,Anil,Pawan'

// join() method also join the arrays into the string
users.join('');// Output will be 'RohitAnilPawan'

